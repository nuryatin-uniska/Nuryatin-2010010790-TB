    public void Warnet() {
        DftTblModel_warnet = new DefaultTableModel();
        DftTblModel_warnet.addColumn("NO PC");
        DftTblModel_warnet.addColumn("SISA WAKTU /menit");
        DftTblModel_warnet.addColumn("KOMENTAR");
        jTable_warnet.setModel(DftTblModel_warnet);
        Connection conn = Konek.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            String SQL = "select * from warnet";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_warnet.addRow(new Object[]{
                    res.getString("no_pc"),
                    res.getString("waktu"),
                    res.getString("comment")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
