    private void jButton_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_hapusActionPerformed
        int baris = jTable_warnet.getSelectedRow();
        jTextField_nopc.setText(DftTblModel_warnet.getValueAt(baris, 0).toString());
        jTextField_waktu.setText(DftTblModel_warnet.getValueAt(baris, 1).toString());
        jTextField_komentar.setText(DftTblModel_warnet.getValueAt(baris, 2).toString());
        Connection conn = Konek.getConnection();
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0) {
            try {
                java.sql.PreparedStatement stmt = conn.prepareStatement("delete from warnet where no_pc ='" + jTextField_nopc.getText() + "'");
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                Warnet();
                jTextField_nopc.setText("");
                jTextField_waktu.setText("");
                jTextField_komentar.setText("");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal di hapus" + e.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton_hapusActionPerformed

